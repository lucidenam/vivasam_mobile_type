package edu.visang.vivasam.opendata.model;

public class MetaCodeSub {
    String code;

    String scode;
    String sname;
    String stotalCnt;

    public String getCode() {
        return code;
    }
    public void setCode(String code) {
        this.code = code;
    }
    public String getScode() {
        return scode;
    }
    public void setScode(String scode) {
        this.scode = scode;
    }
    public String getSname() {
        return sname;
    }
    public void setSname(String sname) {
        this.sname = sname;
    }
    public String getStotalCnt() {
        return stotalCnt;
    }
    public void setStotalCnt(String stotalCnt) {
        this.stotalCnt = stotalCnt;
    }
}
